# Task 4. Roman Numerals

# Get user data
num = int(input("Enter number within the range of 1 through 10: "))

# Calculation
if num == 1:
	print("Roman numeral version of that number is:  I")
elif num == 2:
	print("Roman numeral version of that number is:  II")
elif num == 3:
	print("Roman numeral version of that number is:  III")
elif num == 4:
	print("Roman numeral version of that number is:  IV")
elif num == 5:
	print("Roman numeral version of that number is:  V")
elif num == 6:
	print("Roman numeral version of that number is:  VI")
elif num == 7:
	print("Roman numeral version of that number is:  VII")
elif num == 8:
	print("Roman numeral version of that number is:  VIII")
elif num == 9:
	print("Roman numeral version of that number is:  IX")
elif num == 10:
	print("Roman numeral version of that number is:  X")
else:
	print("Out of range 1-10")